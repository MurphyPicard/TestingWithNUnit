using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace FirstStudio
{
    class NewMath
    {
        public int AddOne(int a)
        {
            return a + 1;
        }

        public int SubOne(int a)
        {
            return a - 1;
        }

        public int MulTwo(int a)
        {
            return a * 2;
        }

        public int DivTwo(int a)
        {
            return a / 2;
        }
    }
}
